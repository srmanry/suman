class Product {
  final String id;
  final String category;
  final String title;
  final String subtitle;
  final double price;
  final String imageUrl;
  final String description;

  const Product({
    required this.id,
    required this.category,
    required this.title,
    required this.subtitle,
    required this.price,
    required this.imageUrl,
    required this.description,
  });
}
